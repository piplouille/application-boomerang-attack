def random_key_generator(key_length):
    """
    Creates a random key with key_length written
    in hexadecimal as string

    Paramaters
    ----------

    key_length : int
        Key length in bits

    Returns
    -------

    key : string
        Key in hexadecimal as string
    """
    return bytes.hex(os.urandom(key_length // 8))


